	public void zeichnen() {
		Graphics zfeld = jPanel1.getGraphics();
		zfeld.clearRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());
		int height = jPanel1.getHeight()
				/ feld.getBlocks().get(0).getVisible_length() + 1;
		int i = 0;
		int i2 = 0;
		for (Spalte sp : feld.getBlocks()) {
			for (Block b : sp.getSpalte()) {
				zfeld.setColor(b.getColor2());
				zfeld.fillRect(height * i, jPanel1.getHeight() - (height * i2)
						- height, height, height);
				i2++;
			}
			i++;
			i2 = 0;
		}
		this.setTitle("" + zaehler);
	}

