	private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jPanel1MouseClicked
		System.out.println(evt.getX() + "/" + evt.getY());
		int height = jPanel1.getHeight()
				/ feld.getBlocks().get(feld.getBlocks().size() - 1)
						.getVisible_length() + 1;
		int x = evt.getX() / height;
		System.out.println(x);
		int y = feld.getBlocks().get(x).getVisible_length() - 1 - evt.getY()
				/ height;
		System.out.println(x + "/" + y);

		ArrayList<Block> e = new ArrayList<>();
		e = feld.CheckMate(e, feld.getBlocks().get(x).getSpalte().get(y), feld
				.getBlocks().get(x).getSpalte().get(y).getColor());
		if (e.size() >= 3) {
			zaehler = zaehler + e.size();
			feld.destroyBlocks(e);

			zeichnen();
		}

	}// GEN-LAST:event_jPanel1MouseClicked

